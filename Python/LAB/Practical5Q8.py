
S1 = {"Red", "Yellow", "Orange", "Blue"}
S2 = {"Violet", "Blue", "Purple"}
union = S1.union(S2)
print("Union of sets S1 and S2:", union)
intersection = S1.intersection(S2)
print("Intersection of sets S1 and S2:", intersection)
differencesetS1S2 = S1.difference(S2)
print("Difference between sets S1 and S2:", differencesetS1S2)
differencesetS2S1 = S2.difference(S1)
print("Difference between sets S2 and S1:", differencesetS2S1)